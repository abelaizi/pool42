#include <unistd.h>

int	ft_atoi(char *str)
{
	int	i;
	int	r;
	int	g;

	i = 0;
	r = 0;
	g = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
	{
		i++;
	}
	while ((str[i] == '+' ) || (str[i] == '-'))
	{
		if (str[i] == '-')
		{
			g *= -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - 48);
		i++;
	}
	return (r * g);
}
